package com.algo1.week4;

public class JavaAutoBoxing {
    public static void main(String[] args) {
        double a = 0.0;
        double b = 0.0;
        Double x = new Double(0.0);
        Double y = new Double(0.0);
        System.out.println(a == b);
        System.out.println(x.equals(y));
    }
}
